using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculadora
{
    public class Operacoes
    {

        public float Somar(int num, int num2)
        {
            return num + num2;
        }

        public float Subtrair(int num, int num2)
        {
            return num - num2;
        }

        public float Multiplicar(int num, int num2)
        {
            return num * num2;
        }

        public float Dividir(int num, int num2)
        {
            return num / num2;
        }


    }
}
